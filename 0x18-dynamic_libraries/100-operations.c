#include "main_100.h"

/**
 * add - The function adds two integers
 * @a: The first integer
 * @b: The second integer
 * Return: The sum of the two numbers
 */

int add(int a, int b)
{
	return (a + b);
}



/**
 * sub - The function adds two integers
 * @a: The first integer
 * @b: The second integer
 * Return: The difference of the two numbers
 */

int sub(int a, int b)
{
	return (a - b);
}


/**
 * mul - The function adds two integers
 * @a: The first integer
 * @b: The second integer
 * Return: The product of the two numbers
 */

int mul(int a, int b)
{
	return (a * b);
}


/**
 * div - The function adds two integers
 * @a: The first integer
 * @b: The second integer
 * Return: The division of the two numbers
 */

int div(int a, int b)
{
	return (a / b);
}



/**
 * mod - The function adds two integers
 * @a: The first integer
 * @b: The second integer
 * Return: The remainder of the two numbers
 */

int mod(int a, int b)
{
	return (a % b);
}
